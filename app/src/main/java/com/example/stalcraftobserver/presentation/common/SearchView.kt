@Composable
fun SearchView(
    query: String = "query",
    onQueryChanged: (String) -> Unit = {},
    modifier: Modifier = Modifier
) {
    TextField(
        value = query,
        onValueChange = onQueryChanged,
        label = { Text("Search") },
        modifier = modifier.padding(12.dp).clip(RoundedCornerShape(14.dp)),
        singleLine = true,
        leadingIcon = {
            Icon(Icons.Default.Search, contentDescription = "Search Icon")
        }
    )
}


